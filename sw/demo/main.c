static void uart_putc(char c)
{
    *(volatile uint32_t *)0x00000000u = (uint32_t)c;
}

// ------------------------------------------------------------
// Bare-metal entry point
// ------------------------------------------------------------

void main(void);

extern int __bss_start, __bss_end;

__attribute__((section(".text._start")))
void _start(void)
{
    /* initialise stack pointer (top of 64 MB DRAM) */
    __asm__ volatile (
        ".option push\n\t"
        ".option norelax\n\t"
        "la   sp, __stack_top\n\t"
        ".option pop\n\t"
    );

    /* zero the BSS segment */
    for (char *p = (char *)&__bss_start; p < (char *)&__bss_end;)
        *p++ = 0;

    /* jump to demo */
    main();

    /* trap if main() returns */
    while (1)
        __asm__ volatile ("wfi");
}

// ------------------------------------------------------------
// Demo main
// ------------------------------------------------------------

void main(void)
{
    npu_dev_t     npu;
    int16_t       result[256];
    int           errors;

    /* 1. initialise NPU */
    npu_init(&npu, 0x10000000u);

    /* 2. run 16×16×16 INT8 GEMM */
    npu_gemm_args_t args = {
        .M             = 16,
        .N             = 16,
        .K             = 16,
        .A             = test_A,
        .B             = test_B,
        .C             = result,
        .a_zp          = 0,
        .b_zp          = 0,
        .out_scale_mul = 1,
        .out_scale_shr = 0,
    };
    npu_rt_gemm(&npu, &args);

    /* 3. compare against golden */
    errors = 0;
    for (int i = 0; i < 256; i++) {
        if (result[i] != golden_C[i])
            errors++;
    }

    /* 4. report result over UART */
    if (errors == 0)
        uart_putc('P');   /* pass */
    else
        uart_putc('F');   /* fail */
}
